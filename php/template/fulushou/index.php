var page_size ={'11':214,'12':188,'13':169,'14':213,'21':200,'22':146,'23':224,'31':191,'32':191,'33':191,'34':191,'35':191,'36':191};
function setImage(idname)
{
    if(reshow == true)
        return;

    while(Onload_imgs_url[image_url_index] == 'not find' || Onload_imgs_url[image_url_index] == 'loading')
    {
        console.log(Onload_imgs_url[image_url_index]);
        image_url_index++;
        if(image_url_index == Onload_imgs_url.length)
            image_url_index = 0;
    }

    var img_bili = image_size_width[image_url_index]/image_size_height[image_url_index];

    var size = page_size[idname];

    var img = id('img'+idname);
    img.src = Onload_imgs_url[image_url_index];
    // console.log(img.src);

    if(img_bili > 1)
    {
        img.style.top = '0px';
        img.style.height = size + 'px';
        img.style.width = size*img_bili + 'px';
        img.style.left = -((size*img_bili-size)/2)+'px';
    }
    else
    {
        img.style.left = '0px';
        img.style.width = size+'px';
        img.style.height = size/img_bili + 'px';
        img.style.top = -((size/img_bili-size)/2) + 'px';
    }

    image_url_index++;
    if(image_url_index==Onload_imgs_url.length)
        image_url_index = 0;
}
